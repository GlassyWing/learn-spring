package org.manlier.propertyeditor;

/**
 * Created by manlier on 2016/10/28.
 */
public class ExoticType {

    private String name;

    public ExoticType(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "ExoticType{" +
                "name='" + name + '\'' +
                '}';
    }
}
